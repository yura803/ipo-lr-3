x = int(input("Введите ваше целое число ")) #попросил пользователя ввести целое число
if (x % 2 == 0): # Добавил оператор ветвления if с проверкой остатка от деления на 2, если число равено 0 то число чётное
    print("Число является чётным") # вывести что число является чётным если условие выполняется
else: 
    print("Число является нечётным") # если условие не выполняется, то выполняется else который выводит что число нечётное